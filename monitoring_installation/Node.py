class Node:

    def set_hostname(self, hostname):
        self.__hostname = hostname

    def get_hostname(self):
        return self.__hostname

    def set_ip(self, address):
        self.__address = address

    def get_ip(self):
        return self.__address

    def set_role(self, role):
        self.__role = role

    def get_role(self):
        return self.__role
